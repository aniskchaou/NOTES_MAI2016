package iit.tn.entity;

/**
 *
 * @author Hsin
 */
public class Groupe {
    public int id;
    public String nom;
    public int id_niveau;

    public Groupe() {
    }

    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getId_niveau() {
        return id_niveau;
    }

    public void setId_niveau(int id_niveau) {
        this.id_niveau = id_niveau;
    }
    
    
}
